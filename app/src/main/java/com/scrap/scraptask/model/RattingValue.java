package com.scrap.scraptask.model;

public class RattingValue {
    public String rattingLable;
    public String rattingValue;
    public String ratingPercentage;

    public String getRattingLable() {
        return rattingLable;
    }

    public void setRattingLable(String rattingLable) {
        this.rattingLable = rattingLable;
    }

    public String getRattingValue() {
        return rattingValue;
    }

    public void setRattingValue(String rattingValue) {
        this.rattingValue = rattingValue;
    }

    public String getRatingPercentage() {
        return ratingPercentage;
    }

    public void setRatingPercentage(String ratingPercentage) {
        this.ratingPercentage = ratingPercentage;
    }
}
